import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";

const repoRoot = process.cwd();
const publicDir = path.join(repoRoot, "public");
const photosDir = path.join(publicDir, "my-photos");
const outDir = path.join(repoRoot, "src", "generated");
const outFile = path.join(outDir, "localPhotos.generated.ts");

const IMAGE_EXTS = new Set([
  ".jpg",
  ".jpeg",
  ".png",
  ".webp",
  ".avif",
  ".gif",
  ".svg",
]);

function isImageFile(filename) {
  return IMAGE_EXTS.has(path.extname(filename).toLowerCase());
}

function toLabel(id) {
  if (!id) return "Photos";
  return id
    .replace(/[-_]+/g, " ")
    .replace(/\b\w/g, (m) => m.toUpperCase());
}

function readDirSafe(dir) {
  try {
    return fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return [];
  }
}

function parseKeywordsValue(raw) {
  if (raw == null) return [];
  const pieces = Array.isArray(raw) ? raw : [raw];
  const split = pieces.flatMap((v) =>
    String(v)
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean)
  );
  return Array.from(new Set(split));
}

function readKeywordsForImage(absoluteImagePath) {
  // macOS: `mdls` exposes Spotlight metadata which often includes XMP/IPTC keywords.
  // If unavailable (or fails), we just return [] and the app will have no keyword filters.
  try {
    const out = execFileSync(
      "mdls",
      ["-raw", "-name", "kMDItemKeywords", absoluteImagePath],
      { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] }
    ).trim();
    if (!out || out === "(null)") return [];
    // `mdls -raw` prints either a string, an array-like format, or a plist-ish value.
    // Heuristic parse:
    // - If it looks like (a, b, c) treat as array.
    // - Else treat as single string.
    if (out.startsWith("(") && out.endsWith(")")) {
      const inner = out.slice(1, -1);
      const items = inner
        .split("\n")
        .map((l) => l.trim())
        .filter(Boolean)
        .map((l) => l.replace(/^[,]+/, "").trim())
        .map((l) => l.replace(/^"(.+)"[,]?$/, "$1"))
        .map((l) => l.replace(/[,]$/, "").trim())
        .filter(Boolean);
      return parseKeywordsValue(items);
    }
    return parseKeywordsValue(out.replace(/^"(.+)"$/, "$1"));
  } catch {
    return [];
  }
}

function listCategory(dirEnt) {
  const categoryId = dirEnt.name;
  const dirPath = path.join(photosDir, dirEnt.name);
  const files = readDirSafe(dirPath)
    .filter((e) => e.isFile() && isImageFile(e.name))
    .map((e) => `${categoryId}/${e.name}`)
    .sort((a, b) => a.localeCompare(b));

  return {
    id: categoryId,
    label: toLabel(categoryId),
    files,
  };
}

function listRootFiles() {
  const files = readDirSafe(photosDir)
    .filter((e) => e.isFile() && isImageFile(e.name))
    .map((e) => e.name)
    .sort((a, b) => a.localeCompare(b));

  return files;
}

function buildCategories() {
  const dirents = readDirSafe(photosDir);
  const folderCats = dirents.filter((e) => e.isDirectory()).map(listCategory);

  const rootFiles = listRootFiles();
  const rootCat =
    rootFiles.length > 0
      ? [{ id: "photos", label: "Photos", files: rootFiles }]
      : [];

  // If the folder doesn't exist yet, still emit a stable shape.
  if (folderCats.length === 0 && rootCat.length === 0) {
    return [{ id: "photos", label: "Photos", files: [] }];
  }

  return [...rootCat, ...folderCats].filter((c) => c.files.length > 0);
}

const categories = buildCategories();

const keywordsByFile = Object.fromEntries(
  categories.flatMap((c) =>
    c.files.map((file) => {
      const abs = path.join(photosDir, file);
      return [file, readKeywordsForImage(abs)];
    })
  )
);

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  outFile,
  `// This file is auto-generated by scripts/generate-local-photos.mjs
// Do not edit manually.

export type GeneratedLocalPhotoCategory = {
  id: string;
  label: string;
  files: string[];
};

export const GENERATED_LOCAL_PHOTO_CATEGORIES: readonly GeneratedLocalPhotoCategory[] = ${JSON.stringify(
    categories,
    null,
    2
  )} as const;

export const GENERATED_LOCAL_PHOTO_KEYWORDS_BY_FILE: Readonly<Record<string, readonly string[]>> = ${JSON.stringify(
    keywordsByFile,
    null,
    2
  )} as const;
`,
  "utf8"
);

console.log(
  `Generated ${path.relative(repoRoot, outFile)} with ${categories.reduce((n, c) => n + c.files.length, 0)} photos.`
);

